#include "libft.h"

char	*ft_strdup(const char *s1)
{
	int		i;
	char	*ptr;
	int		len;

	i = 0;
	while (s1[i])
		i++;
	len = i;
	ptr = (char *)malloc(len + 1);
	if (ptr == NULL)
		return (NULL);
	i = 0;
	while (s1[i])
	{
		ptr[i] = s1[i];
		i++;
	}
	ptr[i] = '\0';
	return (ptr);
}
